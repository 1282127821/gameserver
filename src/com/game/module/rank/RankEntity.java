package com.game.module.rank;

public class RankEntity {

	private int type;
	private int playerId;
	private IRankCA ca;

	public RankEntity() {
	}

	public RankEntity(int type, int playerId, IRankCA ca) {
		this.type = type;
		this.playerId = playerId;
		this.ca = ca;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getPlayerId() {
		return playerId;
	}

	public void setPlayerId(int playerId) {
		this.playerId = playerId;
	}

	public IRankCA getCa() {
		return ca;
	}

	public void setCa(IRankCA ca) {
		this.ca = ca;
	}

}
