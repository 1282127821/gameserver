package com.game.module.copy;

public class TraverseMap {

	private int mapId;// 物品ID
	private int copyId;// 对应副本ID
	private int goodsCfgId;//物品ID
	private int[] affixs;// 拥有的词缀

	public int getMapId() {
		return mapId;
	}

	public void setMapId(int mapId) {
		this.mapId = mapId;
	}

	public int getCopyId() {
		return copyId;
	}

	public void setCopyId(int copyId) {
		this.copyId = copyId;
	}

	public int getGoodsCfgId() {
		return goodsCfgId;
	}

	public void setGoodsCfgId(int goodsCfgId) {
		this.goodsCfgId = goodsCfgId;
	}

	public int[] getAffixs() {
		return affixs;
	}

	public void setAffixs(int[] affixs) {
		this.affixs = affixs;
	}

}
