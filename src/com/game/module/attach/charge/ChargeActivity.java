package com.game.module.attach.charge;

import java.util.ArrayList;

import com.game.module.attach.Attach;

/**
 * 累计充值活动
 */
public class ChargeActivity extends Attach {

	private int charge;
	private ArrayList<Integer> records=new ArrayList<Integer>();
	
	public int getCharge() {
		return charge;
	}
	public void setCharge(int charge) {
		this.charge = charge;
	}
	public ArrayList<Integer> getRecords() {
		return records;
	}
	public void setRecords(ArrayList<Integer> records) {
		this.records = records;
	}
}
