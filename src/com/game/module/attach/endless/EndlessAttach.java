package com.game.module.attach.endless;

import com.game.module.attach.Attach;

public class EndlessAttach extends Attach {

	private int currLayer;// 当前层数
	private int maxLayer;// 历史最高层数
	private int challenge;// 可挑战次数
	private int refresh;// 可刷新次数
	private long clearTime;// 扫荡时间
//	private List<Integer> 

	public EndlessAttach(){}
	
	public EndlessAttach(int playerId, byte type){
		super(playerId, type);
	}
	
	public int getCurrLayer() {
		return currLayer;
	}

	public void setCurrLayer(int currLayer) {
		this.currLayer = currLayer;
	}

	public int getMaxLayer() {
		return maxLayer;
	}

	public void setMaxLayer(int maxLayer) {
		this.maxLayer = maxLayer;
	}

	public int getChallenge() {
		return challenge;
	}

	public void setChallenge(int challenge) {
		this.challenge = challenge;
	}

	public int getRefesh() {
		return refresh;
	}

	public void setRefesh(int refesh) {
		this.refresh = refesh;
	}

	public long getClearTime() {
		return clearTime;
	}

	public void setClearTime(long clearTime) {
		this.clearTime = clearTime;
	}

}
