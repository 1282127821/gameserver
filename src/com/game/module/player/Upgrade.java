package com.game.module.player;
/**  
 * 升级相关的
 */
public class Upgrade {

	private int lev;
	private int exp;
	
	public int getLev() {
		return lev;
	}
	public void setLev(int lev) {
		this.lev = lev;
	}
	public int getExp() {
		return exp;
	}
	public void setExp(int exp) {
		this.exp = exp;
	}
	
	
}
